def setup(progress=None):
    """First-run (and re-run) setup in one call. Returns (ok, lines).

    progress, when given, is called with each line as it happens so UIs can
    stream the transcript. Existing config is kept; every step is safe to
    repeat.
    """
    import sys as _sys
    _sys.path.insert(0, str(Path(__file__).resolve().parent))
    import switchyard_client as swc

    lines = []

    def log(text):
        lines.append(text)
        if progress:
            try:
                progress(text)
            except Exception:
                pass

    bin_path = find_switchyard_bin()
    if not bin_path:
        log("✗ switchyard executable not found — install NeMo Switchyard,")
        log("  then set it once: /switchyard bin <path>  (or export SWITCHYARD_BIN)")
        return False, lines
    log(f"✓ switchyard bin: {bin_path}")

    if CONFIG_PATH.exists():
        opts = load_last_opts()
        log("✓ config exists — keeping it")
    else:
        opts = dict(DEFAULTS)
        write_config(opts)
        log("✓ wrote default config (strong=opus 4.8 · weak=nemotron ultra)")

    ok, report = preflight_report(preflight())
    for line in report.splitlines():
        log("  " + line)
    if not ok:
        log("✗ fix the failing keys above, then run setup again")
        return False, lines

    state = router_state()
    port = str((state or {}).get("port") or opts.get("port", DEFAULTS["port"]))
    if state and state.get("running"):
        log(f"✓ router already running (pid {state['pid']}, port {port})")
    else:
        keys = config_key_envs(CONFIG_PATH)
        started, msg = start_router(bin_path, CONFIG_PATH, opts.get("port", DEFAULTS["port"]),
                                    keys[0] if keys else "")
        if not started:
            log("✗ " + msg)
            return False, lines
        port = str(opts.get("port", DEFAULTS["port"]))
        log("⏳ router starting (~15s — it fetches the upstream catalog first)")
        import urllib.request as _rq
        deadline = time.time() + 75
        while time.time() < deadline:
            try:
                _rq.urlopen(f"http://127.0.0.1:{port}/health", timeout=1)
                break
            except Exception:
                time.sleep(2)
        else:
            log(f"✗ router not healthy after 75s — see {LOG_PATH}")
            return False, lines
        log(f"✓ router healthy on http://127.0.0.1:{port}")

    root = swc.detect(f"http://127.0.0.1:{port}/v1", timeout=3.0)
    if root:
        rts, _default = swc.routes(root)
        okc, msg = connect_provider(root + "/v1", [r["id"] for r in rts])
        log(("✓ " if okc else "✗ ") + msg)
    else:
        log("✗ router up but fingerprint failed — check the port")
        return False, lines

    log("✓ all set — pick the model `switchyard` (/model switchyard)")
    return True, lines


# ---------------------------------------------------------------------------
# CLI — lets agents (and humans) drive setup from the shell:
#   python3 sw_config.py init [key=value ...]
#   python3 sw_config.py start [bin=PATH] [config=PATH] [port=N]
#   python3 sw_config.py stop | status | connect [URL] | disconnect
# Mirrors the /switchyard slash commands 1:1.
# ---------------------------------------------------------------------------

def _cli(argv):
    import sys as _sys
    _sys.path.insert(0, str(Path(__file__).resolve().parent))
    cmd = argv[0] if argv else "help"
    rest = argv[1:]

    if cmd == "init":
        opts, unknown = parse_kv(" ".join(rest))
        if unknown:
            return 1, "unrecognized: " + " ".join(unknown) + "\nknown keys: " + " ".join(sorted(KNOWN_KEYS))
        path = write_config(opts)
        return 0, (f"wrote {path}\nroute: switchyard — classifier {opts['classifier']} picks between "
                   f"strong ({opts['strong']}) and weak ({opts['weak']})\n"
                   f"api key: read from ${opts['key_env']} at router start")

    if cmd == "start":
        kv = dict(tok.split("=", 1) for tok in rest if "=" in tok)
        bin_path = kv.get("bin") or find_switchyard_bin()
        if not bin_path:
            return 1, "switchyard executable not found — pass bin=PATH or export SWITCHYARD_BIN"
        cfg = Path(kv.get("config") or CONFIG_PATH)
        if not cfg.exists():
            return 1, f"no config at {cfg} — run: python3 sw_config.py init"
        key_env = DEFAULTS["key_env"]
        try:
            import re as _re
            m = _re.search(r"\$\{([A-Za-z_][A-Za-z0-9_]*)\}", cfg.read_text())
            if m:
                key_env = m.group(1)
        except Exception:
            pass
        ok, msg = start_router(bin_path, cfg, kv.get("port") or DEFAULTS["port"], key_env)
        return (0 if ok else 1), msg

    if cmd == "stop":
        ok, msg = stop_router()
        return (0 if ok else 1), msg

    if cmd == "preflight":
        cfg = rest[0] if rest else None
        ok, report = preflight_report(preflight(cfg))
        return (0 if ok else 1), report

    if cmd == "setup":
        ok, lines = setup(progress=print)
        return (0 if ok else 1), ""

    if cmd == "status":
        state = router_state()
        payload = {"managed_router": state, "config": str(CONFIG_PATH),
                   "config_exists": CONFIG_PATH.exists(),
                   "switchyard_bin": find_switchyard_bin()}
        return 0, json.dumps(payload, indent=2)

    if cmd == "connect":
        import switchyard_client as swc
        url = rest[0] if rest else None
        if not url:
            state = router_state()
            if state and state.get("running"):
                url = f"http://127.0.0.1:{state['port']}/v1"
        if not url:
            return 1, "no url given and no managed router running — connect URL"
        root = swc.detect(url, timeout=2.0)
        if root is None:
            return 1, f"{url} does not fingerprint as a switchyard router (still booting? ~15s)"
        rts, _default = swc.routes(root)
        ok, msg = connect_provider(root + "/v1", [rt["id"] for rt in rts])
        return (0 if ok else 1), msg + f"\nprovider switchyard → {root}/v1 (routes: " + ", ".join(rt["id"] for rt in rts) + ")"

    if cmd == "disconnect":
        ok, msg = disconnect_provider()
        return (0 if ok else 1), msg

    return 1, ("usage: sw_config.py init [k=v ...] | start [bin= config= port=] | stop | "
               "status | connect [URL] | disconnect")


if __name__ == "__main__":
    import sys as _sys
