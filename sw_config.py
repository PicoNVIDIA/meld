def setup(progress=None):
    """First-run (and re-run) setup in one call. Returns (ok, lines).

    progress, when given, is called with each line as it happens so UIs can
    stream the transcript. Existing config is kept; every step is safe to
    repeat.
    """
    import sys as _sys
    _sys.path.insert(0, str(Path(__file__).resolve().parent))
    import switchyard_client as swc

    lines = []

    def log(text):
        lines.append(text)
        if progress:
            try:
                progress(text)
            except Exception:
                pass

    # Step 0 — self-heal enablement. Agents sometimes install without
    # --enable, and the installer's interactive prompt can't be answered in
    # a non-TTY shell; an unenabled plugin never loads.
    def _meld_enabled():
        enabled, disabled = plugins_buckets()
        return "nemo-switchyard" in enabled and "nemo-switchyard" not in disabled
    if not _meld_enabled():
        hermes_bin = shutil.which("hermes")
        if hermes_bin:
            res = subprocess.run([hermes_bin, "plugins", "enable", "nemo-switchyard"],
                                 capture_output=True, text=True, timeout=60)
            if _meld_enabled():
                log("✓ plugin enabled (it was installed but not enabled)")
            else:
                log("✗ plugin is not enabled and auto-enable failed — run: hermes plugins enable nemo-switchyard")
                log("  " + (res.stderr or res.stdout).strip()[:200])
                return False, lines
        else:
            log("✗ plugin is not enabled — run: hermes plugins enable nemo-switchyard, then retry")
            return False, lines
    else:
        log("✓ plugin enabled")

    bin_path = find_switchyard_bin()
    if not bin_path:
        py = _hermes_python()
        if py:
            log("⏳ installing the router package (nemo-switchyard[server]) into the hermes venv…")
            subprocess.run([py, "-m", "pip", "install", "--quiet", "nemo-switchyard[server]"],
                           capture_output=True, text=True, timeout=600)
            bin_path = find_switchyard_bin()
    if not bin_path:
        log("✗ router not installed and auto-install failed (Linux has prebuilt wheels;")
        log("  macOS may need a source build). Install it, then point me at it:")
        log("  pip install 'nemo-switchyard[server]'   → then run setup again")
        log("  or /router bin <path-to-switchyard>  (or export SWITCHYARD_BIN)")
        return False, lines
    log(f"✓ router binary: {bin_path}")

    # Telemetry library (NeMo Relay) — install so it's ready, but telemetry
    # itself stays OFF until the user opts in (/telemetry on). Non-fatal.
    try:
        import sys as _s
        _s.path.insert(0, str(Path(__file__).resolve().parent))
        import sw_telemetry
        lib_ok, ver = sw_telemetry.relay_lib()
        if lib_ok:
            log(f"✓ telemetry library present ({ver}) — off until /telemetry on")
        else:
            py = _hermes_python()
            if py:
                log("⏳ installing telemetry library (nemo-relay) into the hermes venv…")
                res = subprocess.run(
                    [py, "-m", "pip", "install", "--quiet", "nemo-relay>=0.5,<1.0"],
                    capture_output=True, text=True, timeout=300)
                lib_ok, ver = sw_telemetry.relay_lib()
                if lib_ok:
                    log(f"✓ telemetry library installed ({ver}) — off until /telemetry on")
                else:
                    log("○ telemetry library unavailable from the package index — telemetry stays off")
            else:
                log("○ hermes venv not found — skipping telemetry library install")
    except Exception:
        log("○ telemetry library step skipped")

    if CONFIG_PATH.exists():
        opts = load_last_opts()
        log("✓ config exists — keeping it")
    else:
        opts = dict(DEFAULTS)
        write_config(opts)
        log("✓ wrote default config (strong=opus 4.8 · weak=nemotron ultra)")

    ok, report = preflight_report(preflight())
    for line in report.splitlines():
        log("  " + line)
    if not ok:
        log("✗ fix the failing keys above, then run setup again — or run /router build")
        log("  to compose tiers from endpoints you already have keys for")
        return False, lines

    state = router_state()
    port = str((state or {}).get("port") or opts.get("port", DEFAULTS["port"]))
    if state and state.get("running"):
        log(f"✓ router already running (pid {state['pid']}, port {port})")
    else:
        keys = config_key_envs(CONFIG_PATH)
        started, msg = start_router(bin_path, CONFIG_PATH, opts.get("port", DEFAULTS["port"]),
                                    keys[0] if keys else "")
        if not started:
            log("✗ " + msg)
            return False, lines
        port = str(opts.get("port", DEFAULTS["port"]))
        log("⏳ router starting (~15s — it fetches the upstream catalog first)")
        import urllib.request as _rq
        deadline = time.time() + 75
        while time.time() < deadline:
            try:
                _rq.urlopen(f"http://127.0.0.1:{port}/health", timeout=1)
                break
            except Exception:
                time.sleep(2)
        else:
            log(f"✗ router not healthy after 75s — see {LOG_PATH}")
            return False, lines
        log(f"✓ router healthy on http://127.0.0.1:{port}")

    root = swc.detect(f"http://127.0.0.1:{port}/v1", timeout=3.0)
    if root:
        rts, _default = swc.routes(root)
        okc, msg = connect_provider(root + "/v1", [r["id"] for r in rts])
        log(("✓ " if okc else "✗ ") + msg)
    else:
        log("✗ router up but fingerprint failed — check the port")
        return False, lines

    log("✓ all set — pick the model `router` (/model router)")
    return True, lines


def uninstall():
    """Reverse the integration: stop router, disconnect provider, remove the
    generated config/state. Returns (ok, lines). The plugin itself is left
    for `hermes plugins remove nemo-switchyard` (code shouldn't delete
    itself mid-session); telemetry exports are the user's data and stay."""
    lines = []
    ok_stop, msg = stop_router()
    lines.append(("✓ " if ok_stop else "· ") + msg)
    ok_dc, msg = disconnect_provider()
    lines.append(("✓ " if ok_dc else "· ") + msg)
    if SW_DIR.exists():
        shutil.rmtree(SW_DIR, ignore_errors=True)
        lines.append(f"✓ removed {SW_DIR} (config, state, log)")
    else:
        lines.append("· no generated config to remove")
    lines.append("finish with: hermes plugins remove nemo-switchyard")
    lines.append(f"telemetry exports (if any) kept — delete manually if desired")
    return True, lines


def preset_save(name):
    """Save the current build (last_opts) as a named preset."""
    name = (name or "").strip()
    if not name:
        return False, "usage: preset save <name>"
    data = json.loads(_settings_path().read_text()) if _settings_path().exists() else {}
    if not isinstance(data, dict):
        data = {}
    presets = data.setdefault("presets", {})
    presets[name] = load_last_opts()
    _settings_path().write_text(json.dumps(data, indent=2))
    return True, f"preset '{name}' saved (from the current config)"


def preset_load(name):
    """Write the named preset as the active config. Returns (ok, msg)."""
    name = (name or "").strip()
    presets = (json.loads(_settings_path().read_text()).get("presets")
               if _settings_path().exists() else {}) or {}
    if name not in presets:
        avail = ", ".join(sorted(presets)) or "none saved"
        return False, f"no preset '{name}' — available: {avail}"
    opts = dict(DEFAULTS)
    opts.update({k: v for k, v in presets[name].items() if k in KNOWN_KEYS})
    path = write_config(opts)
    return True, f"preset '{name}' applied → {path} (restart the router to serve it)"


def preset_list():
    presets = (json.loads(_settings_path().read_text()).get("presets")
               if _settings_path().exists() else {}) or {}
    if not presets:
        return "no presets saved — /router preset save <name> stores the current config"
    out = []
    for name, opts in sorted(presets.items()):
        out.append(f"  {name}: strong={_short(opts.get('strong', '?'))}"
                   f" · weak={_short(opts.get('weak', '?'))}")
    return "presets:\n" + "\n".join(out)


def tail_log(n=20):
    try:
        lines = LOG_PATH.read_text(errors="replace").splitlines()
    except Exception:
        return f"no router log at {LOG_PATH}"
    return "\n".join(lines[-max(1, int(n)):])


# ---------------------------------------------------------------------------
# CLI — lets agents (and humans) drive setup from the shell:
#   python3 sw_config.py init [key=value ...]
#   python3 sw_config.py start [bin=PATH] [config=PATH] [port=N]
#   python3 sw_config.py stop | status | connect [URL] | disconnect
# Mirrors the /switchyard slash commands 1:1.
# ---------------------------------------------------------------------------

def _cli(argv):
    import sys as _sys
    _sys.path.insert(0, str(Path(__file__).resolve().parent))
    cmd = argv[0] if argv else "help"
    rest = argv[1:]

    if cmd == "init":
        opts, unknown = parse_kv(" ".join(rest))
        if unknown:
            return 1, "unrecognized: " + " ".join(unknown) + "\nknown keys: " + " ".join(sorted(KNOWN_KEYS))
        path = write_config(opts)
        return 0, (f"wrote {path}\nroute: router — classifier {opts['classifier']} picks between "
                   f"strong ({opts['strong']}) and weak ({opts['weak']})\n"
                   f"api key: read from ${opts['key_env']} at router start")

    if cmd == "start":
        kv = dict(tok.split("=", 1) for tok in rest if "=" in tok)
        bin_path = kv.get("bin") or find_switchyard_bin()
        if not bin_path:
            return 1, "switchyard executable not found — pass bin=PATH or export SWITCHYARD_BIN"
        cfg = Path(kv.get("config") or CONFIG_PATH)
        if not cfg.exists():
            return 1, f"no config at {cfg} — run: python3 sw_config.py init"
        key_env = DEFAULTS["key_env"]
        try:
            import re as _re
            m = _re.search(r"\$\{([A-Za-z_][A-Za-z0-9_]*)\}", cfg.read_text())
            if m:
                key_env = m.group(1)
        except Exception:
            pass
        ok, msg = start_router(bin_path, cfg, kv.get("port") or DEFAULTS["port"], key_env)
        return (0 if ok else 1), msg

    if cmd == "stop":
        ok, msg = stop_router()
        return (0 if ok else 1), msg

    if cmd == "preflight":
        cfg = rest[0] if rest else None
        ok, report = preflight_report(preflight(cfg))
        return (0 if ok else 1), report

    if cmd == "setup":
        ok, lines = setup(progress=print)
        return (0 if ok else 1), ""

    if cmd == "status":
        state = router_state()
        payload = {"managed_router": state, "config": str(CONFIG_PATH),
                   "config_exists": CONFIG_PATH.exists(),
                   "switchyard_bin": find_switchyard_bin()}
        return 0, json.dumps(payload, indent=2)

    if cmd == "connect":
        import switchyard_client as swc
        url = rest[0] if rest else None
        if not url:
            state = router_state()
            if state and state.get("running"):
                url = f"http://127.0.0.1:{state['port']}/v1"
        if not url:
            return 1, "no url given and no managed router running — connect URL"
        root = swc.detect(url, timeout=2.0)
        if root is None:
            return 1, f"{url} does not fingerprint as a switchyard router (still booting? ~15s)"
        rts, _default = swc.routes(root)
        ok, msg = connect_provider(root + "/v1", [rt["id"] for rt in rts])
        return (0 if ok else 1), msg + f"\nprovider router → {root}/v1 (routes: " + ", ".join(rt["id"] for rt in rts) + ")"

    if cmd == "disconnect":
        ok, msg = disconnect_provider()
        return (0 if ok else 1), msg

    if cmd == "restart":
        ok, msg = restart_router()
        return (0 if ok else 1), msg

    if cmd == "uninstall":
        ok, out = uninstall()
        return (0 if ok else 1), "\n".join(out)

    if cmd == "logs":
        return 0, tail_log(rest[0] if rest else 20)

    if cmd == "preset":
        sub = rest[0] if rest else "list"
        if sub == "save":
            ok, msg = preset_save(rest[1] if len(rest) > 1 else "")
            return (0 if ok else 1), msg
        if sub == "list":
            return 0, preset_list()
        ok, msg = preset_load(sub)
        return (0 if ok else 1), msg

    if cmd == "telemetry":
        import sw_telemetry
        sub = rest[0] if rest else "status"
        if sub in ("on", "off"):
            ok, msg = sw_telemetry.toggle()
            return (0 if ok else 1), msg
        if sub == "sessions":
            return 0, sw_telemetry.sessions_report()
        if sub == "view":
            return 0, sw_telemetry.view_report(rest[1] if len(rest) > 1 else "")
        return 0, sw_telemetry.status_report()

    return 1, ("usage: sw_config.py setup | init [k=v ...] | build-see-/router | "
               "start [bin= config= port=] | stop | restart | status | preflight | "
               "connect [URL] | disconnect | logs [n] | preset [list|save <name>|<name>] | "
               "telemetry [status|on|off|sessions|view <n>] | uninstall")


if __name__ == "__main__":
    import sys as _sys
    _code, _msg = _cli(_sys.argv[1:])
    print(_msg)
    _sys.exit(_code)
