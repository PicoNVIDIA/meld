HERMES_CONFIG = Path(os.environ.get("HERMES_HOME", str(Path.home() / ".hermes"))) / "config.yaml"


def plugins_buckets(config_path=None):
    """(enabled, disabled) plugin-name sets from the config's plugins block.

    Substring checks can't tell `enabled:` from `disabled:` entries — and the
    deny-list wins — so every enablement check must go through this.
    """
    path = Path(config_path) if config_path else HERMES_CONFIG
    try:
        lines = path.read_text().splitlines()
    except Exception:
        return set(), set()
    section, bucket = None, None
    enabled, disabled = set(), set()
    for line in lines:
        s = line.strip()
        if line and not line[0].isspace():
            section = s[:-1] if s.endswith(":") else None
            bucket = None
            continue
        if section != "plugins" or not s:
            continue
        if s.startswith(("enabled:", "disabled:")):
            bucket = "enabled" if s.startswith("enabled:") else "disabled"
            inline = s.split(":", 1)[1].strip()
            if inline.startswith("["):
                names = {n.strip() for n in inline.strip("[]").split(",") if n.strip()}
                (enabled if bucket == "enabled" else disabled).update(names)
            continue
        if s.startswith("- ") and bucket:
            (enabled if bucket == "enabled" else disabled).add(s[2:].strip())
    return enabled, disabled


def apply_classifier_followup(opts):
    """When the weak tier leaves the default endpoint and the classifier is
    still the default, classify with the weak model itself so the config only
    needs providers the user actually has. Mutates and returns opts."""
    weak_base = opts.get("weak_base_url") or opts.get("base_url")
    if opts.get("classifier") == DEFAULTS["classifier"] and weak_base != DEFAULTS["base_url"]:
        opts.update({
            "classifier": opts.get("weak", DEFAULTS["weak"]),
            "classifier_base_url": weak_base,
            "classifier_key_env": opts.get("weak_key_env") or opts.get("key_env"),
            "classifier_format": opts.get("weak_format", "openai"),
        })
    return opts


_MARK_BEGIN = "  # >>> router provider — managed by /router connect >>>"
_MARK_END = "  # <<< router provider — remove with /router disconnect <<<"


def _provider_block(url, route_ids):
    models = "[" + ", ".join(route_ids) + "]" if route_ids else "[]"
    return "\n".join([
        _MARK_BEGIN,
        "  router:",
        "    name: Router",
        f"    base_url: {url}",
        "    key_env: ROUTER_API_KEY",
        f"    models: {models}",
        _MARK_END,
    ])


def connect_provider(url, route_ids, config_path=None):
    """Add a `router` entry under `providers:` in config.yaml.

    Marker-bounded and comment-preserving: only the managed block is ever
    touched. Re-running replaces the managed block (url/routes refresh).
    Returns (ok, message).
    """
    path = Path(config_path) if config_path else HERMES_CONFIG
    if not path.exists():
        return False, f"hermes config not found at {path}"
    text = path.read_text()
    block = _provider_block(url, route_ids)

    if _MARK_BEGIN in text and _MARK_END in text:
        head, _, rest = text.partition(_MARK_BEGIN)
        _, _, tail = rest.partition(_MARK_END)
        text = head + block + tail
        path.write_text(text)
        return True, "updated existing router provider entry"

    lines = text.splitlines()
    for i, line in enumerate(lines):
        stripped = line.strip()
        if stripped in ("providers: {}", "providers: {}  # none"):
            lines[i] = "providers:\n" + block
            path.write_text("\n".join(lines) + ("\n" if text.endswith("\n") else ""))
            return True, "added router provider (replaced empty providers: {})"
        if stripped == "providers:":
            lines[i] = line + "\n" + block
            path.write_text("\n".join(lines) + ("\n" if text.endswith("\n") else ""))
            return True, "added router provider under existing providers: key"
    path.write_text(text.rstrip("\n") + "\n\nproviders:\n" + block + "\n")
    return True, "added providers: section with router entry"


def disconnect_provider(config_path=None):
    """Remove the managed provider block. Returns (ok, message)."""
    path = Path(config_path) if config_path else HERMES_CONFIG
    if not path.exists():
        return False, f"hermes config not found at {path}"
    text = path.read_text()
    if _MARK_BEGIN not in text or _MARK_END not in text:
        return False, "no managed router provider entry found"
    head, _, rest = text.partition(_MARK_BEGIN)
    _, _, tail = rest.partition(_MARK_END)
    lines = (head + tail.lstrip("\n")).splitlines()
    # collapse a now-childless `providers:` key back to `providers: {}`
    for i, line in enumerate(lines):
        if line.strip() == "providers:":
            nxt = next((x for x in lines[i + 1:] if x.strip()), "")
            if not nxt.startswith(" "):
                lines[i] = "providers: {}"
            break
    path.write_text("\n".join(lines) + "\n")
    return True, "router provider entry removed"


