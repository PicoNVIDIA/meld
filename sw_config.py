"""Config builder + local router process management for /switchyard.

Generates a Switchyard routes YAML from key=value overrides on top of
NVIDIA-friendly defaults (strong = Opus 4.8 via anthropic format, weak =
Nemotron Ultra, Nano classifier), and starts/stops a local router with it.
Everything lives under ~/.hermes/switchyard/ (user-owned — plugin updates
never touch it). API keys are referenced as ${ENV_VAR} in the YAML and are
never written to disk.
"""
from __future__ import annotations

import json
import os
import re
import shutil
import signal
import subprocess
import time
from pathlib import Path

SW_DIR = Path(os.environ.get("HERMES_HOME", str(Path.home() / ".hermes"))) / "switchyard"
CONFIG_PATH = SW_DIR / "routes.yaml"
STATE_PATH = SW_DIR / "router-state.json"
LOG_PATH = SW_DIR / "router.log"

DEFAULTS = {
    "strong": "aws/anthropic/bedrock-claude-opus-4-8",
    "strong_format": "anthropic",
    "weak": "nvidia/nvidia/nemotron-3-ultra",
    "weak_format": "openai",
    "classifier": "nvidia/nvidia/Nemotron-3-Nano-30B-A3B",
    "base_url": "https://inference-api.nvidia.com/v1",
    "key_env": "NVIDIA_API_KEY",
    "port": "4100",
    "profile": "coding_agent",
    "min_confidence": "0.6",
    "timeout_secs": "600",
    # Per-tier endpoint overrides (empty = inherit the globals above). Set by
    # /switchyard build when tiers come from different connected providers.
    "strong_base_url": "",
    "strong_key_env": "",
    "weak_base_url": "",
    "weak_key_env": "",
    "classifier_base_url": "",
    "classifier_key_env": "",
    "classifier_format": "openai",
}
KNOWN_KEYS = set(DEFAULTS)


def parse_kv(raw_args):
    """'strong=x weak=y port=4200' -> (opts dict, unknown keys list)."""
    opts, unknown = dict(DEFAULTS), []
    for tok in (raw_args or "").split():
        if "=" not in tok:
            unknown.append(tok)
            continue
        k, v = tok.split("=", 1)
        k, v = k.strip().lower(), v.strip()
        if k in KNOWN_KEYS and v:
            opts[k] = v
        else:
            unknown.append(tok)
    return opts, unknown


def _short(model):
    return str(model).rsplit("/", 1)[-1]


def _tier_block(opts, tier, indent, extra=()):
    """YAML lines for one tier target, honoring per-tier endpoint overrides."""
    pad = " " * indent
    base_url = opts.get(f"{tier}_base_url") or opts["base_url"]
    key_env = opts.get(f"{tier}_key_env") or opts["key_env"]
    lines = [
        f"{pad}model: {opts[tier]}",
        f"{pad}api_key: ${{{key_env}}}",
        f"{pad}base_url: {base_url}",
    ]
    lines.extend(f"{pad}{line}" for line in extra)
    return "\n".join(lines)


def build_yaml(opts):
    strong = _tier_block(opts, "strong", 6, (
        f"format: {opts['strong_format']}",
        f"timeout_secs: {opts['timeout_secs']}",
    ))
    weak = _tier_block(opts, "weak", 6, (
        f"format: {opts['weak_format']}",
        f"timeout_secs: {opts['timeout_secs']}",
    ))
    classifier = _tier_block(opts, "classifier", 6, (
        "timeout_secs: 120",
        f"min_confidence: {opts['min_confidence']}",
        "fail_open: true",
        "recent_turn_window: 4",
    ))
    return f"""# Switchyard routes — generated by /switchyard init|build (edit freely)
# strong = {opts['strong']}   weak = {opts['weak']}
# API keys come from environment variables at router start; none are stored.

defaults:
  api_key: ${{{opts['key_env']}}}
  base_url: {opts['base_url']}
  format: openai

routes:
  switchyard:                        # the one model users pick — classifier
    type: deterministic              # routes each request to a tier behind it
    profile: {opts['profile']}
    enable_stats: true
    fallback_target_on_evict: strong
    classifier:
{classifier}
    strong:
      id: strong
{strong}
    weak:
      id: weak
{weak}

# Pinning to one model needs no extra routes: every upstream catalog model is
# also exposed as a passthrough route (e.g. /model {opts['strong']}).
"""


def write_config(opts, path=None):
    path = Path(path) if path else CONFIG_PATH
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(build_yaml(opts))
    _save_last_opts(opts)
    return path


def _settings_path():
    return Path(__file__).resolve().parent / "settings.json"


def _save_last_opts(opts):
    """Remember the last build (model ids, endpoints, env NAMES — no secrets)
    so the interactive panel can edit one tier without re-asking everything."""
    try:
        data = json.loads(_settings_path().read_text())
        if not isinstance(data, dict):
            data = {}
    except Exception:
        data = {}
    data["last_opts"] = {k: v for k, v in opts.items() if k in KNOWN_KEYS}
    try:
        _settings_path().write_text(json.dumps(data, indent=2))
    except Exception:
        pass


def load_last_opts():
    """Last written config opts, over defaults."""
    opts = dict(DEFAULTS)
    try:
        data = json.loads(_settings_path().read_text())
        saved = data.get("last_opts") or {}
        opts.update({k: v for k, v in saved.items() if k in KNOWN_KEYS})
    except Exception:
        pass
    return opts


# ---------------------------------------------------------------------------
# upstream key preflight
# ---------------------------------------------------------------------------

_TIER_RE = re.compile(
    r"model:\s*(\S+)\s*\n\s*api_key:\s*\$\{([A-Za-z_][A-Za-z0-9_]*)\}"
    r"\s*\n\s*base_url:\s*(\S+)(?:\s*\n\s*format:\s*(\S+))?")


def preflight(config_path=None):
    """Probe every (model, base_url, key_env) tier via a 1-token completion.

    Returns a list of dicts {model, base_url, key_env, status}: HTTP code
    (200 ok, 401/403 key rejected, 4xx model unavailable), "missing" when the
    env var has no value anywhere, or "unreachable". Keys come from the
    environment or $HERMES_HOME/.env and are sent only to their endpoint.
    """
    import sys as _sys
    _sys.path.insert(0, str(Path(__file__).resolve().parent))
    import switchyard_client as swc

    path = Path(config_path) if config_path else CONFIG_PATH
    try:
        text = path.read_text()
    except Exception:
        return []
    tiers = sorted({(m.group(1), m.group(3), m.group(2), m.group(4) or "openai")
                    for m in _TIER_RE.finditer(text)})
    results = []
    for model, base_url, key_env, fmt in tiers:
        value = os.environ.get(key_env) or _key_from_hermes_env_file(key_env)
        if not value:
            status = "missing"
        else:
            code = swc.probe_upstream(base_url, value, model, fmt)
            status = code if code is not None else "unreachable"
        results.append({"model": model, "base_url": base_url,
                        "key_env": key_env, "status": status})
    return results


def preflight_report(results):
    """PASS/FAIL lines for preflight() output; returns (all_ok, text)."""
    if not results:
        return True, "no upstream targets found in config"
    lines, ok = [], True
    for r in results:
        where = f"{r['model']} @ {r['base_url']}"
        if r["status"] == 200:
            lines.append(f"PASS  {where} (${r['key_env']})")
        elif r["status"] in (401, 403):
            ok = False
            lines.append(f"FAIL  ${r['key_env']} rejected by {r['base_url']} for {r['model']} "
                         f"(HTTP {r['status']}) — this endpoint may need a different key type")
        elif r["status"] == "missing":
            ok = False
            lines.append(f"FAIL  ${r['key_env']} has no value (env or ~/.hermes/.env)")
        elif isinstance(r["status"], int):
            ok = False
            lines.append(f"FAIL  {where}: HTTP {r['status']} — key accepted but model unavailable")
        else:
            lines.append(f"WARN  {where} not reachable for preflight")
    return ok, "\n".join(lines)


# ---------------------------------------------------------------------------
# provider entry in config.yaml (/switchyard connect|disconnect)
# ---------------------------------------------------------------------------

HERMES_CONFIG = Path(os.environ.get("HERMES_HOME", str(Path.home() / ".hermes"))) / "config.yaml"
_MARK_BEGIN = "  # >>> switchyard provider — managed by /switchyard connect >>>"
_MARK_END = "  # <<< switchyard provider — remove with /switchyard disconnect <<<"


def _provider_block(url, route_ids):
    models = "[" + ", ".join(route_ids) + "]" if route_ids else "[]"
    return "\n".join([
        _MARK_BEGIN,
        "  switchyard:",
        "    name: Switchyard",
        f"    base_url: {url}",
        "    key_env: SWITCHYARD_API_KEY",
        f"    models: {models}",
        _MARK_END,
    ])


def connect_provider(url, route_ids, config_path=None):
    """Add a `switchyard` entry under `providers:` in config.yaml.

    Marker-bounded and comment-preserving: only the managed block is ever
    touched. Re-running replaces the managed block (url/routes refresh).
    Returns (ok, message).
    """
    path = Path(config_path) if config_path else HERMES_CONFIG
    if not path.exists():
        return False, f"hermes config not found at {path}"
    text = path.read_text()
    block = _provider_block(url, route_ids)

    if _MARK_BEGIN in text and _MARK_END in text:
        head, _, rest = text.partition(_MARK_BEGIN)
        _, _, tail = rest.partition(_MARK_END)
        text = head + block + tail
        path.write_text(text)
        return True, "updated existing switchyard provider entry"

    lines = text.splitlines()
    for i, line in enumerate(lines):
        stripped = line.strip()
        if stripped in ("providers: {}", "providers: {}  # none"):
            lines[i] = "providers:\n" + block
            path.write_text("\n".join(lines) + ("\n" if text.endswith("\n") else ""))
            return True, "added switchyard provider (replaced empty providers: {})"
        if stripped == "providers:":
            lines[i] = line + "\n" + block
            path.write_text("\n".join(lines) + ("\n" if text.endswith("\n") else ""))
            return True, "added switchyard provider under existing providers: key"
    path.write_text(text.rstrip("\n") + "\n\nproviders:\n" + block + "\n")
    return True, "added providers: section with switchyard entry"


def disconnect_provider(config_path=None):
    """Remove the managed provider block. Returns (ok, message)."""
    path = Path(config_path) if config_path else HERMES_CONFIG
    if not path.exists():
        return False, f"hermes config not found at {path}"
    text = path.read_text()
    if _MARK_BEGIN not in text or _MARK_END not in text:
        return False, "no managed switchyard provider entry found"
    head, _, rest = text.partition(_MARK_BEGIN)
    _, _, tail = rest.partition(_MARK_END)
    lines = (head + tail.lstrip("\n")).splitlines()
    # collapse a now-childless `providers:` key back to `providers: {}`
    for i, line in enumerate(lines):
        if line.strip() == "providers:":
            nxt = next((x for x in lines[i + 1:] if x.strip()), "")
            if not nxt.startswith(" "):
                lines[i] = "providers: {}"
            break
    path.write_text("\n".join(lines) + "\n")
    return True, "switchyard provider entry removed"


