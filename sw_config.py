def find_switchyard_bin(settings=None):
    """settings['switchyard_bin'] > $SWITCHYARD_BIN > PATH lookup.

    With settings=None, falls back to the plugin's own settings.json (so the
    shell CLI honors a bin saved via /switchyard bin).
    """
    if settings is None:
        try:
            settings = json.loads((Path(__file__).resolve().parent / "settings.json").read_text())
        except Exception:
            settings = {}
    for cand in (
        (settings or {}).get("switchyard_bin"),
        os.environ.get("SWITCHYARD_BIN"),
        shutil.which("switchyard"),
    ):
        if cand and Path(cand).exists():
            return str(cand)
    return None


def _read_state():
    try:
        return json.loads(STATE_PATH.read_text())
    except Exception:
        return None


def _write_state(state):
    STATE_PATH.parent.mkdir(parents=True, exist_ok=True)
    STATE_PATH.write_text(json.dumps(state, indent=2))


def _pid_alive(pid):
    """True when *pid* is a live process. Zombies count as dead — a router
    started from inside a long-lived Hermes session is never reaped by it,
    so kill(0) alone would report the corpse as running forever."""
    try:
        os.kill(int(pid), 0)
    except Exception:
        return False
    try:
        out = subprocess.run(["ps", "-p", str(pid), "-o", "stat="],
                             capture_output=True, text=True, timeout=5)
        return not out.stdout.strip().startswith("Z")
    except Exception:
        return True


def router_state():
    """{'pid', 'port', 'config', 'running', 'stale'} or None if never started.

    ``stale`` — the routes config on disk is newer than the running process,
    i.e. saved changes are not applied until a restart.
    """
    state = _read_state()
    if not state:
        return None
    state["running"] = _pid_alive(state.get("pid", -1))
    stale = False
    if state["running"]:
        try:
            stale = Path(state.get("config", CONFIG_PATH)).stat().st_mtime > float(
                state.get("started_at", 0))
        except Exception:
            stale = False
    state["stale"] = stale
    return state


def restart_router():
    """Stop the managed router, wait, start it on the current config.

    Returns (ok, message)."""
    state = router_state()
    if state and state.get("running"):
        ok, msg = stop_router()
        if not ok:
            return False, msg
        deadline = time.time() + 15
        while time.time() < deadline and _pid_alive(state.get("pid", -1)):
            time.sleep(0.3)
    bin_path = find_switchyard_bin()
    if not bin_path:
        return False, "switchyard executable not found — /switchyard bin <path>"
    opts = load_last_opts()
    keys = config_key_envs(CONFIG_PATH)
    ok, msg = start_router(bin_path, CONFIG_PATH, opts.get("port", DEFAULTS["port"]),
                           keys[0] if keys else "")
    return ok, ("restarted with the saved config — " + msg if ok else msg)


def _key_from_hermes_env_file(key_env):
    """Read KEY=value from $HERMES_HOME/.env (hermes's own env file).

    Agent terminal sessions run with a scrubbed environment, so the exported
    var may be invisible here even when the user has it set. The .env file is
    the sanctioned durable location; we only read it, never write it.
    """
    env_file = Path(os.environ.get("HERMES_HOME", str(Path.home() / ".hermes"))) / ".env"
    try:
        for line in env_file.read_text().splitlines():
            line = line.strip()
            if not line or line.startswith("#") or "=" not in line:
                continue
            k, _, v = line.partition("=")
            if k.strip().removeprefix("export ").strip() == key_env:
                return v.strip().strip("\"'") or None
    except Exception:
        pass
    return None


_ENV_REF_RE_LOCAL = re.compile(r"\$\{([A-Za-z_][A-Za-z0-9_]*)\}")


def config_key_envs(config_path):
    """All ${VAR} names referenced by the routes config."""
    try:
        return sorted(set(_ENV_REF_RE_LOCAL.findall(Path(config_path).read_text())))
    except Exception:
        return []


def start_router(bin_path, config_path, port, key_env):
    """Spawn `switchyard --routing-profiles <cfg> -- serve` detached.

    Returns (ok, message). Every key env var the config references must be set
    in this environment or present in $HERMES_HOME/.env (agent shells are
    env-scrubbed); values are passed to the router process only, never
    written anywhere.
    """
    state = router_state()
    if state and state.get("running"):
        return False, f"router already running (pid {state['pid']}, port {state['port']}) — /switchyard stop first"
    child_env = dict(os.environ)
    needed = config_key_envs(config_path) or ([key_env] if key_env else [])
    missing = []
    for var in needed:
        if child_env.get(var):
            continue
        from_file = _key_from_hermes_env_file(var)
        if from_file:
            child_env[var] = from_file
        else:
            missing.append(var)
    if missing:
        pretty = ", ".join(missing)
        return False, (f"{pretty} not set here and not in $HERMES_HOME/.env — "
                       f"add `<VAR>=<value>` lines to ~/.hermes/.env (chmod 600) or export them, then retry")
    SW_DIR.mkdir(parents=True, exist_ok=True)
    log = open(LOG_PATH, "a")
    try:
        proc = subprocess.Popen(
            [bin_path, "--routing-profiles", str(config_path), "--",
             "serve", "--host", "127.0.0.1", "--port", str(port)],
            stdout=log, stderr=subprocess.STDOUT,
            stdin=subprocess.DEVNULL, start_new_session=True, env=child_env,
        )
    except Exception as exc:
        return False, f"failed to start router: {exc}"
    _write_state({"pid": proc.pid, "port": int(port), "config": str(config_path),
                  "started_at": time.time()})
    return True, (f"router starting (pid {proc.pid}) on http://127.0.0.1:{port} — "
                  f"takes ~15s to bind (upstream catalog fetch); log: {LOG_PATH}")


def stop_router():
    state = router_state()
    if not state:
        return False, "no managed router recorded (only routers started via /switchyard start are managed)"
    if not state.get("running"):
        return False, f"managed router (pid {state.get('pid')}) is not running"
    try:
        os.kill(int(state["pid"]), signal.SIGTERM)
    except Exception as exc:
        return False, f"failed to stop pid {state.get('pid')}: {exc}"
    for _ in range(20):
        if not _pid_alive(state["pid"]):
            break
        time.sleep(0.1)
    alive = _pid_alive(state["pid"])
    return (not alive), ("router stopped" if not alive else f"SIGTERM sent to pid {state['pid']} (still shutting down)")


